    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id_bodega");
        modelo.addColumn("tipo_bodega");
        modelo.addColumn("Nombre_bodega");
        modelo.addColumn("Direccion");
        modelo.addColumn("Estado");
       
        
        bodegasDAO clienteDAO = new bodegasDAO();
        List<bodegas> cliente = clienteDAO.select();
        tablaAula.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < cliente.size(); i++) {
            dato[0] = Integer.toString(cliente.get(i).getPkid());
            dato[1] = cliente.get(i).getTipo_de_bodega();
            dato[2] = cliente.get(i).getNombre_bode();
            dato[3] = cliente.get(i).getDireccion_bode();
            dato[4] = cliente.get(i).getEstado_bode();
          
         
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

